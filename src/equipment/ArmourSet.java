package equipment;

public class ArmourSet
{
	private HeadArmour headArmour;
	private TorsoArmour torsoArmour;
	private LegArmour legArmour;
	private ForearmArmour forearmArmour;
	
	public HeadArmour getHeadArmour ()
	{
		return headArmour;
	}
	public void setHeadArmour ( HeadArmour headArmour )
	{
		this.headArmour = headArmour;
	}
	public TorsoArmour getTorsoArmour ()
	{
		return torsoArmour;
	}
	public void setTorsoArmour ( TorsoArmour torsoArmour )
	{
		this.torsoArmour = torsoArmour;
	}
	public LegArmour getLegArmour ()
	{
		return legArmour;
	}
	public void setLegArmour ( LegArmour legArmour )
	{
		this.legArmour = legArmour;
	}
	public ForearmArmour getForearmArmour ()
	{
		return forearmArmour;
	}
	public void setForearmArmour ( ForearmArmour forearmArmour )
	{
		this.forearmArmour = forearmArmour;
	}
	
	
}
